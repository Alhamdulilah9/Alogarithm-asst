function analyzeSentence(sentence) {
  let length = 0;
  let wordCount = 0;
  let vowelCount = 0;
  const vowels = "aeiouAEIOU";
  
  let inWord = false;
  
  for (let char of sentence) {
      if (char === '.') {
          break;
      }
      length++;
      if (vowels.includes(char)) {
          vowelCount++;
      }
      if (char === ' ') {
          if (inWord) {
              wordCount++;
              inWord = false;
          }
      } else {
          inWord = true;
      }
  }
  
  if (inWord) {
      wordCount++;
  }
  
  return { length, wordCount, vowelCount };
}

// Example usage
const sentence = "This is a sample sentence.";
const result = analyzeSentence(sentence);
console.log(`Length: ${result.length}, Words: ${result.wordCount}, Vowels: ${result.vowelCount}`);
